let arr = [1, 2, 3];

/**
 * unshift will add a value to the beginning
 * return value will be the # of elements
 */

console.log('arr.unshift(0)', arr.unshift(0));
console.log('arr', arr);

/**
 * shift will retrieve the value at the beginning
 * return value will be the retrieved value
 */
console.log('arr.shift()', arr.shift());
console.log('arr', arr);

/**
 * push will add a value to the end
 * return value will be the # of elements
 */
console.log('arr.push(1)', arr.push(0));
console.log('arr', arr);

/**
 * pop will retrieve the value at the end
 * return value will be the retrieved value
 */
console.log('arr.pop()', arr.pop());
console.log('arr', arr);
