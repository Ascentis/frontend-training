let arr = [1, 2, 3];

// unshif will add a value to the beginning
console.log('arr.unshift(1)', arr.unshift(4));
console.log('arr', arr);
console.log('arr.shift()', arr.shift());
console.log('arr', arr);

// push will add a value to the beginning
console.log('arr.push(1)', arr.push(4));
console.log('arr', arr);
console.log('arr.pop()', arr.pop());
console.log('arr', arr);
